#Programa para calcular a área de um retangulo

print ("## CALCULADORA DE ÁREA DE UM RETANGULO  ##")

#Entrada de dados

base = float(input("Qual é a medida da base do retângulo? (cm) "))

altura = float(input("Qual é a medida da altura do retêngulo? (cm) "))

#Processamento dos dados

area = base * altura

#Saida de dados

print ("A área do retêngulo é de %.2f centímetros." % area)

