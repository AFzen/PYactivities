#Média de notas do aluno

#Entrada de dados

aluno = (str(input("Qual é o seu nome? ")))

n1 = (float(input("Qual é a sua 1° nota? ")))

n2 = (float(input("Qual é a sua 2° nota? ")))

n3 = (float(input("Qual é a sua 3° nota? ")))

#Processamento

media = ((n1+n2+n3)/3)

#Saída de dados

print ("A média final de", aluno, "é: %.2f " %media)





